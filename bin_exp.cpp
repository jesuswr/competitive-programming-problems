unsigned long long pow( unsigned long long b , unsigned long long e )
{
	if ( e == 1 ) return b;
	if ( e == 0 ) return 1;
	unsigned long long ret;
	ret = pow(b,e/2);
	ret *= ret;
	if ( e&1 ) ret *= b;
	return ret;
}

long long binpow(long long a, long long b) {
    if (b == 0)
        return 1;
    long long res = binpow(a, b / 2);
    if (b % 2)
        return res * res * a;
    else
        return res * res;
}


